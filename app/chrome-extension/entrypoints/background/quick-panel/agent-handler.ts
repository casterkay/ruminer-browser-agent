function normalizeString(value: unknown): string {
  return typeof value === 'string' ? value : '';
}

function isValidCliPreference(value: unknown): value is AgentCliPreference {
  return (
    value === 'openclaw' ||
    value === 'claude' ||
    value === 'codex' ||
    value === 'cursor' ||
    value === 'qwen' ||
    value === 'glm'
  );
}

function getQuickSessionEngine(sessionIdRaw: string): AgentCliPreference {
  const sessionId = normalizeString(sessionIdRaw).trim();
  if (!sessionId) return QUICK_PANEL_DEFAULT_ENGINE_NAME;
  return quickSessionEngineBySessionId.get(sessionId) || QUICK_PANEL_DEFAULT_ENGINE_NAME;
}

function initQuickSessionEngine(
  sessionIdRaw: string,
  options?: { forceNew?: boolean },
): AgentCliPreference {
  const sessionId = normalizeString(sessionIdRaw).trim();
  if (!sessionId) return QUICK_PANEL_DEFAULT_ENGINE_NAME;

  if (options?.forceNew) {
    quickSessionEngineBySessionId.set(sessionId, QUICK_PANEL_DEFAULT_ENGINE_NAME);
    return QUICK_PANEL_DEFAULT_ENGINE_NAME;
  }

  const existing = quickSessionEngineBySessionId.get(sessionId);
  if (existing) return existing;

  quickSessionEngineBySessionId.set(sessionId, QUICK_PANEL_DEFAULT_ENGINE_NAME);
  return QUICK_PANEL_DEFAULT_ENGINE_NAME;
}

function normalizePort(value: unknown): number | null {
  const num =
    typeof value === 'number' ? value : typeof value === 'string' ? Number(value) : Number.NaN;

  if (!Number.isFinite(num)) return null;

  const port = Math.floor(num);
  if (port <= 0 || port > 65535) return null;

  return port;
}

function normalizeBool(value: unknown): boolean {
  if (value === true) return true;
  if (value === false) return false;
  if (typeof value === 'string') {
    const v = value.trim().toLowerCase();
    if (v === 'true' || v === '1' || v === 'yes' || v === 'on') return true;
    if (v === 'false' || v === '0' || v === 'no' || v === 'off') return false;
  }
  return false;
}

function createRequestId(): string {
  // Prefer crypto.randomUUID for proper UUID format
  try {
    const id = crypto?.randomUUID?.();
    if (id) return id;
  } catch {
    // Fallback for environments without crypto.randomUUID
  }
  return `req_${Date.now()}_${Math.random().toString(16).slice(2)}`;
}

function sleep(ms: number): Promise<void> {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

function isTerminalStatus(status: string): boolean {
  return status === 'completed' || status === 'error' || status === 'cancelled';
}

