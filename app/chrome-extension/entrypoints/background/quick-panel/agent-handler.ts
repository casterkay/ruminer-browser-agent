/**
 * Initialize the Quick Panel Agent Handler.
 * Sets up message listeners and tab cleanup handlers.
 */
export function initQuickPanelAgentHandler(): void {
  if (initialized) return;
  initialized = true;

  // Track sidepanel presence via a port from the sidepanel UI.
  chrome.runtime.onConnect.addListener((port) => {
    if (port?.name !== 'ruminer_sidepanel_presence') return;

    let tabId: number | null = null;

    port.onMessage.addListener((msg: any) => {
      const nextTabId = typeof msg?.tabId === 'number' ? msg.tabId : null;
      if (typeof nextTabId === 'number' && Number.isFinite(nextTabId)) {
        tabId = nextTabId;
        sidepanelPresenceByTabId.set(nextTabId, { connectedAt: Date.now() });
      }
    });

    port.onDisconnect.addListener(() => {
      if (typeof tabId === 'number') sidepanelPresenceByTabId.delete(tabId);
    });
  });

  // Message listener for Quick Panel messages
  chrome.runtime.onMessage.addListener((message, sender, sendResponse) => {
    // Handle QUICK_PANEL_SEND_TO_AI
    if (message?.type === BACKGROUND_MESSAGE_TYPES.QUICK_PANEL_SEND_TO_AI) {
      handleSendToAI(message as QuickPanelSendToAIMessage, sender)
        .then(sendResponse)
        .catch((err) => {
          const msg = err instanceof Error ? err.message : String(err);
          sendResponse({ success: false, error: msg || 'Unknown error' });
        });
      return true; // Async response
    }

    // Handle QUICK_PANEL_CANCEL_AI
    if (message?.type === BACKGROUND_MESSAGE_TYPES.QUICK_PANEL_CANCEL_AI) {
      handleCancelAI(message as QuickPanelCancelAIMessage, sender)
        .then(sendResponse)
        .catch((err) => {
          const msg = err instanceof Error ? err.message : String(err);
          sendResponse({ success: false, error: msg || 'Unknown error' });
        });
      return true; // Async response
    }

    // Handle QUICK_PANEL_OPEN_SIDEPANEL
    if (message?.type === BACKGROUND_MESSAGE_TYPES.QUICK_PANEL_OPEN_SIDEPANEL) {
      handleOpenSidepanel(message, sender)
        .then(sendResponse)
        .catch((err) => {
          const msg = err instanceof Error ? err.message : String(err);
          sendResponse({ success: false, error: msg || 'Unknown error' });
        });
      return true; // Async response
    }

    // Handle QUICK_PANEL_ACTIVATE_SESSION
    if (message?.type === BACKGROUND_MESSAGE_TYPES.QUICK_PANEL_ACTIVATE_SESSION) {
      handleActivateSession(message as QuickPanelActivateSessionMessage, sender)
        .then(sendResponse)
        .catch((err) => {
          const msg = err instanceof Error ? err.message : String(err);
          sendResponse({ success: false, error: msg || 'Unknown error' });
        });
      return true; // Async response
    }

    // Handle QUICK_PANEL_OPEN_SESSION
    if (message?.type === BACKGROUND_MESSAGE_TYPES.QUICK_PANEL_OPEN_SESSION) {
      handleOpenSession(message as QuickPanelOpenSessionMessage)
        .then(sendResponse)
        .catch((err) => {
          const msg = err instanceof Error ? err.message : String(err);
          sendResponse({ success: false, error: msg || 'Unknown error' });
        });
      return true; // Async response
    }

    // Handle QUICK_PANEL_DELETE_SESSION
    if (message?.type === BACKGROUND_MESSAGE_TYPES.QUICK_PANEL_DELETE_SESSION) {
      handleDeleteSession(message as QuickPanelDeleteSessionMessage)
        .then(sendResponse)
        .catch((err) => {
          const msg = err instanceof Error ? err.message : String(err);
          sendResponse({ success: false, error: msg || 'Unknown error' });
        });
      return true; // Async response
    }

    // Handle QUICK_PANEL_PERSIST_SESSION
    if (message?.type === BACKGROUND_MESSAGE_TYPES.QUICK_PANEL_PERSIST_SESSION) {
      handlePersistSession(message as QuickPanelPersistSessionMessage)
        .then(sendResponse)
        .catch((err) => {
          const msg = err instanceof Error ? err.message : String(err);
          sendResponse({ success: false, error: msg || 'Unknown error' });
        });
      return true; // Async response
    }

    // Handle QUICK_PANEL_GET_BRANDING
    if (message?.type === BACKGROUND_MESSAGE_TYPES.QUICK_PANEL_GET_BRANDING) {
      handleGetBranding(message as QuickPanelGetBrandingMessage)
        .then(sendResponse)
        .catch((err) => {
          const msg = err instanceof Error ? err.message : String(err);
          sendResponse({ success: false, error: msg || 'Unknown error' });
        });
      return true; // Async response
    }

    // Handle QUICK_PANEL_SET_SESSION_ENGINE
    if (message?.type === BACKGROUND_MESSAGE_TYPES.QUICK_PANEL_SET_SESSION_ENGINE) {
      handleSetSessionEngine(message as QuickPanelSetSessionEngineMessage, sender)
        .then(sendResponse)
        .catch((err) => {
          const msg = err instanceof Error ? err.message : String(err);
          sendResponse({ success: false, error: msg || 'Unknown error' });
        });
      return true; // Async response
    }

    return false;
  });

  // Clean up requests when their tab is closed
  chrome.tabs.onRemoved.addListener((tabId) => {
    for (const [requestId, request] of activeRequests) {
      if (request.tabId === tabId) {
        cleanupRequest(requestId, 'tab_removed');
      }
    }
  });

  console.debug(`${LOG_PREFIX} Initialized`);
}
